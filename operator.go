// operator Aritmatika
a := 10
b := 3
fmt.Println(a + b) // Output: 13
fmt.Println(a % b) // Output: 1

// operator perbandingan
x := 5
y := 7
fmt.Println(x < y)  // true
fmt.Println(x == y) // false

// operator logika
a := true
b := false
fmt.Println(a && b) // false
fmt.Println(a || b) // true
fmt.Println(!a)     // false

